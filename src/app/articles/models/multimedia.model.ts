/**
 * Multimedia type contains article media (images)
 */
export interface Multimedia {
    /** media caption */
    caption: string;
    /** media copyright */
    copyright: string;
    /** media format */
    format: string;
    /** media height */
    height: number;
    /** media width */
    width: number;
    /** media type */
    type: string;
    /** media url */
    url: string;
}
